package algscom.experiment.it.model;

public class Quota {
	
	private String esito;
	private Double quota;
	
	public Quota() {
		super();
	}

	public String getEsito() {
		return esito;
	}

	public void setEsito(String esito) {
		this.esito = esito;
	}

	public Double getQuota() {
		return quota;
	}

	public void setQuota(Double quota) {
		this.quota = quota;
	}
}
